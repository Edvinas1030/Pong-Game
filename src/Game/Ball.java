package Game;

public class Ball {
    private int ballX=2, ballY=5, ballDX=1, ballDY=-1, ballSpeed=1;

    public int getBallX() {
        return ballX;
    }

    public void setBallX(int ballX) {
        this.ballX = ballX;
    }

    public int getBallY() {
        return ballY;
    }

    public void setBallY(int ballY) {
        this.ballY = ballY;
    }

    public int getBallDX() {
        return ballDX;
    }

    public void setBallDX(int ballDX) {
        this.ballDX = ballDX;
    }

    public int getBallDY() {
        return ballDY;
    }

    public void setBallDY(int ballDY) {
        this.ballDY = ballDY;
    }

    public int getBallSpeed() {
        return ballSpeed;
    }

    public void setBallSpeed(int ballSpeed) {
        this.ballSpeed = ballSpeed;
    }
    
    
}
